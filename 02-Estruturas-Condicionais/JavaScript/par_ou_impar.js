// Objetivo: Verificar se um número é par ou ímpar usando o operador Módulo (%)

let numero = 20; // Variável que será testada

if (numero % 2 == 0) {
    console.log("O número é Par.");
} else {
    console.log("O número é Ímpar.");
}

// Conceito: A condição 'numero % 2 == 0' verifica se o resto da divisão por 2 é zero.
// Dica de Sintaxe: O uso de '==' é crucial para comparação (diferente do '=' para atribuição).
