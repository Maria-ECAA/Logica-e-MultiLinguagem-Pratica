// Objetivo: Verificar se um número é par ou ímpar usando o operador Módulo (%)

class ParOuImpar {
    public static void main(String[] args) {
        int numero = 7; // Variável que será testada

        if (numero % 2 == 0) {
            System.out.println("O número é Par.");
        } else {
            System.out.println("O número é Ímpar.");
        }
    }
}

// Conceito: Diferente do JavaScript, o Java exige a declaração do tipo (int).
// Dica de Sintaxe: O código deve estar dentro de uma classe e do método 'main'.
